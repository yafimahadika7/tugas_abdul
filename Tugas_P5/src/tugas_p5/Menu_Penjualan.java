    private void ppnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ppnActionPerformed
        try {
            // Ambil nilai harga dari JTextField harga (hasil dari hargaActionPerformed)
            int hargaValue = Integer.parseInt(harga.getText());

            // Hitung PPN 10% dari harga
            double ppnValue = hargaValue * 0.1;  // 10% dari harga

            // Gunakan DecimalFormat untuk menampilkan angka tanpa pemisah ribuan
            DecimalFormat formatter = new DecimalFormat("#");
            ppn.setText(formatter.format(ppnValue));  // Format angka tanpa pemisah ribuan

        } catch (NumberFormatException e) {
            // Tampilkan pesan jika format data salah
            ppn.setText("Input tidak valid");
            System.out.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_ppnActionPerformed

