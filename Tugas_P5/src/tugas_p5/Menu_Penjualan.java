    private void hargaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hargaActionPerformed
        try {
            // Ambil nilai dari luasTanahAsli, hargaTanah, dan hargaBangunan
            int luasTanahAsliValue = Integer.parseInt(luasTanahAsli.getText());  // Ambil luas tanah asli
            int hargaTanahPerM2Value = Integer.parseInt(hargaTanah.getText());  // Ambil harga tanah per m2
            int hargaBangunanValue = Integer.parseInt(hargaBangunan.getText());  // Ambil harga bangunan

            // Hitung harga total berdasarkan rumus: (Luas Tanah Asli * Harga Tanah/M2) + Harga Bangunan
            int totalHarga = (luasTanahAsliValue * hargaTanahPerM2Value) + hargaBangunanValue;

            // Tampilkan hasil perhitungan ke dalam harga (di dalam JTextField harga)
            harga.setText(String.valueOf(totalHarga));
        } catch (NumberFormatException e) {
            // Tampilkan pesan jika format data salah
            harga.setText("Input tidak valid");
            System.out.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_hargaActionPerformed

