    private void tampilkanData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Nama Pemesan");
        model.addColumn("Area");
        model.addColumn("Tipe Rumah");
        model.addColumn("Luas Tanah");
        model.addColumn("Harga");
        model.addColumn("Lama Cicilan");
        model.addColumn("Cicilan/Bln");

        try {
            String sql = "SELECT * FROM pemesanan"; // Ganti sesuai nama tabel
            Connection conn = KoneksiDB.configDB();
            Statement stmt = conn.createStatement();
            ResultSet res = stmt.executeQuery(sql);

            while (res.next()) {
                model.addRow(new Object[]{
                    res.getString("nama_pemesan"),
                    res.getString("area"),
                    res.getString("tipe_rumah"),
                    res.getString("luas_tanah"),
                    res.getString("harga"),
                    res.getString("lama_cicilan"),
                    res.getString("cicilan_per_bulan")
                });
            }
            jTable1.setModel(model);
        } catch (Exception e) {
            System.out.println("Gagal menampilkan data: " + e.getMessage());
        }
    }

