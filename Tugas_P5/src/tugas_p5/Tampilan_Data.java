        // TODO add your handling code here:
        int total = 0;
    for (int i = 0; i < jTable1.getRowCount(); i++) {
        String hargaStr = jTable1.getValueAt(i, 4).toString().replaceAll("[^\\d]", ""); // kolom ke-4 = Harga
        int harga = Integer.parseInt(hargaStr);
        total += harga;
    }
    totalPenjualan.setText(String.valueOf(total));
